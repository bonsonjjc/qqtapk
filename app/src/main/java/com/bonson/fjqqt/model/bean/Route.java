package com.bonson.fjqqt.model.bean;

public class Route {
    private String fid;
    private String ftmobile;
    private String fseqid;
    private String fxx;
    private String fyy;
    private String futime;
    private String fctime;
    private String fdx;
    private String fdy;

    public String getFid() {
        return fid;
    }

    public void setFid(String fid) {
        this.fid = fid;
    }

    public String getFtmobile() {
        return ftmobile;
    }

    public void setFtmobile(String ftmobile) {
        this.ftmobile = ftmobile;
    }

    public String getFseqid() {
        return fseqid;
    }

    public void setFseqid(String fseqid) {
        this.fseqid = fseqid;
    }

    public String getFxx() {
        return fxx;
    }

    public void setFxx(String fxx) {
        this.fxx = fxx;
    }

    public String getFyy() {
        return fyy;
    }

    public void setFyy(String fyy) {
        this.fyy = fyy;
    }

    public String getFutime() {
        return futime;
    }

    public void setFutime(String futime) {
        this.futime = futime;
    }

    public String getFctime() {
        return fctime;
    }

    public void setFctime(String fctime) {
        this.fctime = fctime;
    }

    public String getFdx() {
        return fdx;
    }

    public void setFdx(String fdx) {
        this.fdx = fdx;
    }

    public String getFdy() {
        return fdy;
    }

    public void setFdy(String fdy) {
        this.fdy = fdy;
    }
}
